public class OperatorArithmetic {
    public static void main(String[] args) {
        int a = 10;
        int b = 5;
        //Prioridades -> * , /, %, +, -
        int soma = (a + b);
        int mult = a * b;
        int subi = a - b;
        int divi = a / b;

        System.out.println(soma);
        System.out.println(mult);
        System.out.println(divi);
        System.out.println(subi);
    }
}
